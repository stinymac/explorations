package org.mac.explorations.corejava.base.generic.usage;

/**
 * from <<Think in Java>>
 *
 * 包含两个对象的元组对象
 *
 * @auther mac
 * @date 2019-10-04
 */

public class TwoTuple<T,O> {

    public final T first;
    public final O second;

    public TwoTuple(T first, O second) {
        this.first = first;
        this.second = second;
    }

    @Override
    public String toString() {
        return "TwoTuple{" +
                "first=" + first +
                ", second=" + second +
                '}';
    }
}
