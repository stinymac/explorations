package org.mac.explorations.corejava.concurrent.util.atomic;

import sun.misc.Unsafe;

import java.lang.reflect.Field;

/**
 * @auther mac
 * @date 2019-12-02
 */
public final class UnsafeInstance {

    private UnsafeInstance(){throw new UnsupportedOperationException();}

    public static Unsafe get() {
        try {
            Field theUnsafe = Unsafe.class.getDeclaredField("theUnsafe");
            theUnsafe.setAccessible(true);
            return (Unsafe) theUnsafe.get(null);
        } catch (Exception e) {
            throw new RuntimeException("get Unsafe instance fail!");
        }
    }
}
