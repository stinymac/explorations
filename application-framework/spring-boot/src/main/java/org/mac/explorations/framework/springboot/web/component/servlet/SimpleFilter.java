package org.mac.explorations.framework.springboot.web.component.servlet;

import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.stereotype.Component;

import javax.servlet.Filter;
import javax.servlet.FilterChain;
import javax.servlet.ServletException;
import javax.servlet.ServletRequest;
import javax.servlet.ServletResponse;
import java.io.IOException;

/**
 * @auther mac
 * @date 2020-02-03 20:33
 */
//@Component
public class SimpleFilter implements Filter {
    public static final Logger LOGGER = LoggerFactory.getLogger(SimpleFilter.class);

    @Override
    public void doFilter(ServletRequest request, ServletResponse response, FilterChain chain) throws IOException, ServletException {
        LOGGER.info("------->This is a filter in spring-boot web application");
        chain.doFilter(request,response);
    }
}
