package org.mac.explorations.framework.mybatis.mbg;

import org.mybatis.generator.api.MyBatisGenerator;
import org.mybatis.generator.config.Configuration;
import org.mybatis.generator.config.xml.ConfigurationParser;
import org.mybatis.generator.internal.DefaultShellCallback;

import java.io.File;
import java.nio.file.Path;
import java.nio.file.Paths;
import java.util.ArrayList;
import java.util.List;

/**
 * @auther mac
 * @date 2020-01-10
 */
public class AutomaticReverseGenerationExample {

    public static void runGenerator() {
        List<String> warnings = new ArrayList<>();
        boolean overwrite = true;
        File configFile = new File(Thread.currentThread().getContextClassLoader().getResource("configuration/generatorConfiguration.xml").getPath());
        ConfigurationParser cp = new ConfigurationParser(warnings);
        Configuration config = null;
        try {
            config = cp.parseConfiguration(configFile);
            DefaultShellCallback callback = new DefaultShellCallback(overwrite);
            MyBatisGenerator myBatisGenerator = new MyBatisGenerator(config, callback, warnings);
            myBatisGenerator.generate(null);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public static void main(String[] args) {
        runGenerator();
    }
}
